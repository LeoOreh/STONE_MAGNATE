using System.Collections.Generic;

public class UPDATE_TS_RES : RES_DATA
{
    //-----------------------------------------------------------------------------------------------------------------
    //-----------------------------------------------------------------------------------------------------------------
    // ПРОВЕРКА ТАЙМЕРОВ ПОЛУЧЕНИЯ РЕСУРСОВ
    public static void O()
    {
        foreach(KeyValuePair<string, STRCT_RES_TYP> res in RES)
        {
            if (GL.TSG > res.Value.TS_GET)
            {
                res.Value.TS_GET += res.Value.TS_Interval;
                res.Value.SCORE  += res.Value.GET_V;

                GET_RES(res.Key);
            }
        }
    }
    //-----------------------------------------------------------------------------------------------------------------
    //-----------------------------------------------------------------------------------------------------------------



    //-----------------------------------------------------------------------------------------------------------------
    //-----------------------------------------------------------------------------------------------------------------
    // ОБНОВИТЬ ЗНАЧЕНИЕ
    static void GET_RES(string res)
    {
        // добавить анимации

        int n = 1;
        // если на странице есть добываемые ресурсы
        foreach (string r in GL.TYP_MINING)
        {
            if (r == res) 
            {
                UI_RES.UPD_Res(res, n);
            }
            n++;
        }
    }
    //-----------------------------------------------------------------------------------------------------------------
    //-----------------------------------------------------------------------------------------------------------------
}
