using UnityEngine;

public class BUTTON_MINING : RESOURCE
{
    //-----------------------------------------------------------------------------------------------------------------
    Animator animator;
    //-----------------------------------------------------------------------------------------------------------------




    //-----------------------------------------------------------------------------------------------------------------
    private void Start()
    {
        animator = GetComponent<Animator>();
    }
    //-----------------------------------------------------------------------------------------------------------------




    //-----------------------------------------------------------------------------------------------------------------
    // клик основной кнопки по середине для ускорения добычи ресурсов
    public void Click()
    {
        resources[GL.typs_mining_resource[0]].time_get = 0;
        
        animator.SetTrigger("Click");
    }
    //-----------------------------------------------------------------------------------------------------------------
}
